function sortKo(arr) {
  return [...arr].sort((a, b) => a.localeCompare(b, "ko"));
}

function normalize(list, head) {
  return sortKo([
    ...new Set(
      list
        .map((s) => String(s).trim())
        .filter(Boolean)
        .filter((s) => s !== head),
    ),
  ]).slice(0, 5);
}

