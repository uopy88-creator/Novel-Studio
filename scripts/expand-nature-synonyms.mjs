function sortKo(arr) {
  return [...arr].sort((a, b) => a.localeCompare(b, "ko"));
}

function normalize(list, head) {
  return sortKo([
    ...new Set(
      list
        .map((s) => String(s).trim())
        .filter(Boolean)
        .filter((s) => s !== head),
    ),
  ]).slice(0, 5);
}

/** @type {Record<string, string[]>} */
const ADDITIONS = {};

function put(head, syns) {
  const key = String(head).trim();
  if (!key || ADDITIONS[key]) return;
  ADDITIONS[key] = syns;
}

/** 여러 항목을 한 번에 등록 */
function putAll(entries) {
  for (const [h, s] of entries) put(h, s);
}

