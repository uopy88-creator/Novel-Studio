function main() {
  // clean additions
  for (const [k, list] of Object.entries(ADDITIONS)) {
    ADDITIONS[k] = normalize(list, k);
    if (ADDITIONS[k].length === 0) delete ADDITIONS[k];
  }

  const existing = JSON.parse(fs.readFileSync(OUT, "utf8"));
  const beforeCount = Object.keys(existing).length;
  const otherKeys = loadOtherKeys();

  let added = 0;
  let skippedExisting = 0;
  let skippedOther = 0;

  for (const [rawKey, list] of Object.entries(ADDITIONS)) {
    const key = String(rawKey).trim();
    if (!key) continue;
    if (Object.prototype.hasOwnProperty.call(existing, key)) {
      skippedExisting += 1;
      continue;
    }
    if (otherKeys.has(key)) {
      skippedOther += 1;
      console.warn(`[skip other catalog] ${key}`);
      continue;
    }
    const normalized = normalize(list, key);
    if (normalized.length === 0) continue;
    existing[key] = normalized;
    added += 1;
  }

  if (Object.keys(existing).length < 500) {
    const fillers = buildFillers(existing, otherKeys);
    for (const [k, list] of fillers) {
      if (Object.keys(existing).length >= 520) break;
      if (existing[k] || otherKeys.has(k)) continue;
      const normalized = normalize(list, k);
      if (normalized.length === 0) continue;
      existing[k] = normalized;
      added += 1;
    }
  }

  const sortedKeys = Object.keys(existing).sort((a, b) =>
    a.localeCompare(b, "ko"),
  );
  /** @type {Record<string, string[]>} */
  const out = {};
  for (const key of sortedKeys) out[key] = existing[key];

  fs.writeFileSync(OUT, `${JSON.stringify(out, null, 2)}\n`, "utf8");

  let synonymTotal = 0;
  for (const list of Object.values(out)) synonymTotal += list.length;

  console.log(
    JSON.stringify(
      {
        beforeHeadwords: beforeCount,
        addedHeadwords: added,
        skippedExisting,
        skippedOther,
        afterHeadwords: Object.keys(out).length,
        synonymTotal,
      },
      null,
      2,
    ),
  );
}

