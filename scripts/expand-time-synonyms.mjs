function loadOtherKeys() {
  /** @type {Set<string>} */
  const keys = new Set();
  for (const name of fs.readdirSync(SYNONYM_DIR)) {
    if (!name.endsWith(".json") || name === "time.json") continue;
    const data = JSON.parse(
      fs.readFileSync(path.join(SYNONYM_DIR, name), "utf8"),
    );
    for (const k of Object.keys(data)) keys.add(k);
  }
  return keys;
}

