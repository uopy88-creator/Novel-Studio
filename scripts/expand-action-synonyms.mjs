function loadOtherKeys() {
  /** @type {Set<string>} */
  const keys = new Set();
  for (const name of fs.readdirSync(SYNONYM_DIR)) {
    if (!name.endsWith(".json") || name === "action.json") continue;
    const data = JSON.parse(
      fs.readFileSync(path.join(SYNONYM_DIR, name), "utf8"),
    );
    for (const k of Object.keys(data)) keys.add(k);
  }
  return keys;
}

function main() {
  const existing = JSON.parse(fs.readFileSync(OUT, "utf8"));
  const beforeCount = Object.keys(existing).length;
  const otherKeys = loadOtherKeys();

  let added = 0;
  let skippedExisting = 0;
  let skippedOther = 0;

  for (const [rawKey, list] of Object.entries(ADDITIONS)) {
    const key = String(rawKey).trim();
    if (!key) continue;
    // 임시 키(숫자 접미사)는 정리 — 실제 키만 사용
    if (/\d+$/.test(key) && key !== key.replace(/\d+$/, "")) {
      // keep keys that intentionally end with numbers only if needed; skip *2 duplicates
      if (key.endsWith("2") || key.endsWith("3")) {
        // skip duplicate placeholder keys
        continue;
      }
    }
    if (Object.prototype.hasOwnProperty.call(existing, key)) {
      skippedExisting += 1;
      continue;
    }
    if (otherKeys.has(key)) {
      skippedOther += 1;
      console.warn(`[skip other catalog] ${key}`);
      continue;
    }
    const normalized = normalize(list, key);
    if (normalized.length === 0) continue;
    existing[key] = normalized;
    added += 1;
  }

  const sortedKeys = Object.keys(existing).sort((a, b) =>
    a.localeCompare(b, "ko"),
  );
  /** @type {Record<string, string[]>} */
  const out = {};
  for (const key of sortedKeys) {
    out[key] = existing[key];
  }

  fs.writeFileSync(OUT, `${JSON.stringify(out, null, 2)}\n`, "utf8");

  let synonymTotal = 0;
  for (const list of Object.values(out)) synonymTotal += list.length;

  console.log(
    JSON.stringify(
      {
        beforeHeadwords: beforeCount,
        addedHeadwords: added,
        skippedExisting,
        skippedOther,
        afterHeadwords: Object.keys(out).length,
        synonymTotal,
      },
      null,
      2,
    ),
  );
}

main();
