/**
 * =============================================================================
 * Dictionary — 타입
 * -----------------------------------------------------------------------------
 * 단어 탭은 「뜻」만 반환한다. 유의어·예문·추천은 다루지 않는다.
 * =============================================================================
 */

export interface DictionaryLookupResult {
  /** 정규화된 검색어 */
  query: string;
  /** 뜻 (없으면 null) */
  definition: string | null;
  /** 출처 표시용 (예: 표준국어대사전) */
  source?: string;
}

export const DICTIONARY_NOT_FOUND_MESSAGE =
  "사전 정보를 찾을 수 없습니다.";
