/** 따옴표·공백을 걷어내고 사전 검색어로 만든다. */
export function normalizeDictionaryQuery(raw: string): string {
  return raw
    .trim()
    .replace(/^["'“”‘’「」『』]+/, "")
    .replace(/["'“”‘’「」『』]+$/, "")
    .replace(/\s+/g, " ")
    .trim();
}
